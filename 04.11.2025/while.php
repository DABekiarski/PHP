<?php
//Числата от 1 до 10

$k = 1;//Управляваща променлива
//на цикъла к със стойност 1.

//Цикличен алгоритъм while
while($k < 11){//Условие, което докато
//е удовлетворено се изпълнява цикъла
	//Тяло на цикъла
	if($k == 6) break;//Условна конструкция
	// Ако к е равно на 6, излизаме от цикъла
	echo "$k ";
	$k++;//Увеличаваме к с 1
}

echo "<br>";//Нов ред

echo "End of cycle.";

?>